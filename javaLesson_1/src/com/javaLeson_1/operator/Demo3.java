package com.javaLeson_1.operator;

public class Demo3 {
    public static void main(String[] args) {
        //关系运算符的结果是 falls true 布尔值
        int a = 10;
        int b = 20;
        int c = 21;
        System.out.println(c%a); // 21除以10 =2 余  1
        System.out.println(a<b); //true
        System.out.println(a>b);//false
        System.out.println(a==b);//false
        System.out.println(a!=b);//true
    }
}
